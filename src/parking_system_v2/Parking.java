    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/vehicle","root","123");
            
            String sql = "insert into parked values (?, ?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = con.prepareStatement(sql);
            
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jTextField2.getText());
            
            String type = null;
            if(jRadioButton1.isSelected()){
                type = jRadioButton1.getText();
            }
            if(jRadioButton2.isSelected()){
                type = jRadioButton2.getText();
            }
            pst.setString(3, type);
            
            String brand;
            brand = jComboBox1.getSelectedItem().toString();
            
            pst.setString(4, brand);
            pst.setString(5, jTextField3.getText());
            pst.setString(6, jTextField4.getText());
            pst.setString(7, jTextField5.getText());
            pst.setString(8, jTextField7.getText());
            pst.setString(9, jTextField8.getText());
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Data Updated Successfully");
            con.close();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
 
